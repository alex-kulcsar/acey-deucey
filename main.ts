function revealCard () {
    middleCard = deck.nextCard
    middleSprite.setImage(deck.getCardImage(middleCard, CardSpriteSize.ThirtyTwoByThirtyTwo))
    leftFace = leftCard.pipId
    if (leftFace == 0) {
        leftFace = 12.5
    }
    rightFace = rightCard.pipId
    if (rightFace == 0) {
        rightFace = 12.5
    }
    middleFace = middleCard.pipId
    if (middleFace == 0) {
        middleFace = 12.5
    }
    if (leftFace < middleFace && middleFace < rightFace || leftFace > middleFace && middleFace > rightFace) {
        inBetween = 1
    } else {
        inBetween = 0
    }
    if (playerChoosesInBetween == inBetween) {
        music.baDing.play()
        info.changeScoreBy(1)
        waitingOnPlayer = 0
    } else {
        endGame = 1
    }
    nextRoundTime = game.runtime() + 2000
}
